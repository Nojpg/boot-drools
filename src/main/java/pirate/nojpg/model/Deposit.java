package pirate.nojpg.model;

public class Deposit {

	private int sumOfDeposit;
	private int termOfDeposit;

	public Deposit() {
	}

	public int getSumOfDeposit() {
		return sumOfDeposit;
	}

	public void setSumOfDeposit(int sumOfDeposit) {
		this.sumOfDeposit = sumOfDeposit;
	}

	public int getTermOfDeposit() {
		return termOfDeposit;
	}

	public void setTermOfDeposit(int termOfDeposit) {
		this.termOfDeposit = termOfDeposit;
	}
}